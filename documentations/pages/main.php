<h2>About Pages</h2>
<p>Pages are similar to Posts in that they have a title, body text, and associated metadata, the physical differences between posts and pages are that pages dont have extensions like <code>.html</code>, while Posts have those extensions. Pages can have a hierarchy. You can nest Pages under other Pages by making one the "Parent" of the other, creating a group of Pages.</p>
<p>Managing Pages is very similar to managing Posts.</p>
<h3>View Pages Screen</h3>
<p>The <strong>View Pages Screen</strong> provides the facility to manage all the Pages in a site. Via this Screen, Pages can be edited, deleted, and viewed.</p>

<p>The table of Pages contains the following columns:</p>
<ul>
	<li><strong>[]</strong> - Checkbox, normaly used in bulk actions.</li>
	<li>
    	<strong>Name</strong> - This is the Page's Title displayed as a link. Click the Title link to view subpage. Under the Title you can find Action links <em>(see page action description below)</em>.<br />
        <p>The Name column are consists of the following:</p>
        <ul>
            <li><strong>Title</strong> - This is the title of your page.</li>
            <li><strong>Author</strong> - Displays the name of the page author.</li>
            <li><strong>Action Links</strong> - Immediate action links <em>(see page action description below)</em>.</li>
        </ul>
	</li>
	<li><strong>Date</strong> - Displays the page published date and status.<br />
        <p>The Date column are consists of the following:<br />
            <ul>
                <li><strong>Published Date</strong> - This is the published of your page.</li>
                <li><strong>Page Status</strong> - Displays the status of the page. <em>e.g Published,Draft,Trashed</em></li>
            </ul>
        </p>
	</li>
</ul>

<p>The page actions are described below:</p>
<ul>
	<li><strong>Edit</strong> - Load the page to Page Editor, edit action are found just below the Title.</li>
	<li><strong>View</strong> - Display the page as seen on the main site, view action are found just below the Title just between <strong>Edit</strong> and <strong>Trashed</strong>.</li>
	<li><strong>Trashed</strong> - Unpublished the page and mark it as trashed, a trashed page is not physicaly removed from the site but transfered to a recycled bin (similar to windows recycled bin). Where it can either restored or deleted.</li>
</ul>

<h3>Bulk Action</h3>
<p>Currently the available bulk action is <strong>Delete</strong>. You can find the button on the bottom part of the Page View table.</p>
<p>To to a Bulk Delete Action do the following action in order:</p>
<ul class="nostyle">
	<li>1.&nbsp;&nbsp;Select the pages you want to delete by clicking the checkbox found beside the title, or click the checkbox on the column header to select all.</li>
    <li>2.&nbsp;&nbsp;Once you have selected the pages, click the Delete button.</li>
    <li>3.&nbsp;&nbsp;If the action is successfull you will see a message confirming the action is a success, else you have to do it again <em>(go back to step 1)</em>.</li>
</ul>
<p><strong>Note:</strong> The action is not physically removing the page from the system but mainly changing its status to Trashed.</p>

<h3>Sidebar</h3>
<p>The Page sidebar are consists of following buttons:</p>
<ul>
	<li><strong>Add Page</strong> - When click it will take you to the Page Editor, by default the page type is <strong>Page</strong>.</li>
    <li><strong>Trashed</strong> - When click it will take you to the trashbin where all you deleted pages are found.</li>
</ul>