<h2>About Posts</h2>
<p>Posts are similar to Pages in that they have a title, body text, and associated metadata, the physical differences between posts and pages are that post have extensions like <code>.html</code>, while Page dont have. Post can be come a child to a certain page. You can nest Post under other Pages by making one the "Parent" of the other, creating a group of Post.</p>
<p>However you cant turn Post into a parent page, only page has that option.</p>
<p>Managing Posts is very similar to managing Pages.</p>

<h3>View Posts Screen</h3>
<p>The <strong>View Posts Screen</strong> provides the facility to manage all the Posts in a site. Via this Screen, Posts can be edited, deleted, and viewed.</p>
<p>The table of Posts contains the following columns:</p>
<ul>
	<li><strong>Pages</strong> - Pages <em>(in folder icon)</em>, click to load subpages and posts.</li>
	<li><strong>[]</strong> - Checkbox, normaly used in bulk actions.</li>
	<li>
    	<strong>Name</strong> - This is the Posts's Title displayed as a link. Click the Title link to edit the Posts. Under the Title you can find Action links <em>(see post action description below)</em>.<br />
        <p>The Name column are consists of the following:</p>
        <ul>
            <li><strong>Title</strong> - This is the title of your post.</li>
            <li><strong>Author</strong> - Displays the name of the post author.</li>
            <li><strong>Action Links</strong> - Immediate action links <em>(see post action description below)</em>.</li>
        </ul>
	</li>
	<li><strong>Date</strong> - Displays the post published date and status.<br />
        <p>The Date column are consists of the following:<br />
            <ul>
                <li><strong>Published Date</strong> - This is the published of your post.</li>
                <li><strong>Post Status</strong> - Displays the status of the post. <em>e.g Published,Draft,Trashed</em></li>
            </ul>
        </p>
	</li>
</ul>

<p>The post actions are described below:</p>
<ul>
	<li><strong>Edit</strong> - Load the post to Page Editor, edit action are found just below the Title.</li>
	<li><strong>View</strong> - Display the post as seen on the main site, view action are found just below the Title just between <strong>Edit</strong> and <strong>Trashed</strong>.</li>
	<li><strong>Trashed</strong> - Unpublished the post and mark it as trashed, a trashed post is not physicaly removed from the site but transfered to a recycled bin <em>(similar to windows recycled bin)</em>. Where it can either restored or deleted.</li>
</ul>
<h3>Bulk Action</h3>
<p>Currently the available bulk action is <strong>Delete</strong>. You can find the button on the bottom part of the Post View table.</p>
<p>To to a Bulk Delete Action do the following action in order:</p>
<ul class="nostyle">
	<li>1.&nbsp;&nbsp;Select the post you want to delete by clicking the checkbox found beside the title, or click the checkbox on the column header to select all.</li>
    <li>2.&nbsp;&nbsp;Once you have selected the post, click the Delete button.</li>
    <li>3.&nbsp;&nbsp;If the action is successfull you will see a message confirming the action is a success, else you have to do it again <em>(go back to step 1)</em>.</li>
</ul>
<p><strong>Note:</strong> The action is not physically removing the post from the system but mainly changing its status to Trashed.</p>

<h3>Sidebar</h3>
<p>The Posts sidebar are consists of following buttons:</p>
<ul>
	<li><strong>Add Page</strong> - When click it will take you to the Page Editor, by default the page type is <strong>Page</strong>.</li>
    <li><strong>Add Post</strong> - When click it will take you to the Page Editor, where page type is set as <strong>Post</strong>.</li>
    <li><strong>Trashed</strong> - When click it will take you to the trashbin where all you deleted posts are found.</li>
</ul>