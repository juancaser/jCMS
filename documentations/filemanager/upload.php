<h2>About File Manager Upload</h2>
<p>A file manager features that enables you to do multiple uploads to the server, currently only images are allowed to be uploaded.</p>

<h3>Uploading files</h3>
<p>Follow the direction in order:</p>
<ul class="nostyle">
	<li>1.&nbsp;&nbsp;Click <strong>Browse</strong> button to start selecting files you want to upload.</li>
    <li>2.&nbsp;&nbsp;Then click open to add it to the queue lists. To do multiple selection just press  <img src="<?php get_siteinfo('url');?>/documentations/filemanager/images/mac-kb-command.jpg" />  or <strong>CTRL + Select File</strong>.</li>
	<li>3.&nbsp;&nbsp;After selecting and adding it to the queue lists click <strong>Upload Files</strong> button, if successfull it will display the uploaded image.</li>
    <li>4.&nbsp;&nbsp;<strong>Optional.</strong> Click <strong>Clear Queue</strong> to clear the lists and restart your upload.</li>
</ul>
