<h2>About File Manager</h2>
<p>File Manager works like your FTP client, except for the ability to change file permissions. File Explorer enables you to browse you server directory.</p>
<p>In File Manager you can remove and rename a file (carefull on doing this it might cause server problem).</p>

<h3>Accessing the Sub Directory</h3>
<p>Click on the <img src="<?php get_siteinfo('url');?>/backend/modules/filemanager/icon/icon_folder.gif" /> to view its content/subdirectory. To go back to previous directory click on the <strong><img src="<?php get_siteinfo('url');?>/backend/modules/filemanager/icon/icon_back.gif" />&nbsp;Previous</strong> link to go back.</p>

<h3>Directory Action</h3>
<p>File manager has two directory actions, <strong>Rename</strong> and <strong>Delete</strong></p>
<ul>
	<li><strong>Rename</strong> - Renames a file.</li>
	<li><strong>Delete</strong> - Deletes a file.</li>
</ul>
<p><strong>Note:</strong> Any unecessary changes to the filename may cause the whole site to crash.</p>